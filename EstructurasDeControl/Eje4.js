// Función que determina el número mayor entre dos números enteros
function numeroMayor(num1, num2) {
    // Utiliza el operador ternario para determinar el mayor de los dos números
    return num1 > num2 ? num1 : num2;
}

// Ejemplo de uso:
console.log(numeroMayor(10, 5)); // Salida: 10
console.log(numeroMayor(7, 20)); // Salida: 20
